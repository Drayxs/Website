					Since most things nowadays are designed for your hands, your hands are often very busy. Your feet on the other hand are generally fairly free. We wanted to explore a <b>different type of interaction</b> and designed an unlocking interface that moves the interaction from your hands to your feet.<br><br>

					Pressure sensors under the mat detect your movements then transmit the data to the door's lock. When the correct combination is entered, the door is unlocked. <i>Tip Tap Mat</i> can be operated by everyone and is ideal in situations where the can unlock the door without having to put down whatever they're holding, making it a <b>hassle free experience</b>.
